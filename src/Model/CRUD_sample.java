    //CLEARTEXT
    private void cleartext() {
        txt_fullname.setText("");
        txt_address.setText("");
        txt_contact.setText("");
        txt_email.setText("");
        buttonGroup1.clearSelection();
        cmb_job.setSelectedItem("SELECT");
        txt_id.setText("");
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //VALIDATION
        if (txt_fullname.getText().isEmpty()) {
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "Fullname cannot be empty", "WARNING", JOptionPane.ERROR_MESSAGE);
            txt_fullname.requestFocus();

        } else if (txt_contact.getText().isEmpty()) {
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "Contact cannot be empty", "WARNING", JOptionPane.ERROR_MESSAGE);
            txt_fullname.requestFocus();

        } else if (!Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", txt_email.getText())) {
            JOptionPane.showMessageDialog(null, "Please enter a valid email", "Error", JOptionPane.ERROR_MESSAGE);
            getToolkit().beep();
            txt_email.requestFocus();

        } else if (txt_address.getText().isEmpty()) {
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "Address cannot be empty", "WARNING", JOptionPane.ERROR_MESSAGE);
            txt_address.requestFocus();

        } else if (cmb_job.getSelectedItem() == "SELECT") {
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "Job cannot be empty", "WARNING", JOptionPane.ERROR_MESSAGE);
            txt_fullname.requestFocus();
        } else if (radio_male.isSelected() == false && radio_female.isSelected() == false) {
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "Gender cannot be empty", "WARNING", JOptionPane.ERROR_MESSAGE);

        } else {

            try {
                pst = conn.prepareStatement("INSERT INTO `test`.`tbl_crud`(`fullname`, `contact`, `email`, `address`, `job`, `gender`)"
                        + "VALUES(?,?,?,?,?,?)");
                pst.setString(1, txt_fullname.getText());
                pst.setString(2, txt_contact.getText());
                pst.setString(3, txt_email.getText());
                pst.setString(4, txt_address.getText());
                pst.setString(5, cmb_job.getSelectedItem().toString());
                radio_male.setActionCommand("Male");
                radio_female.setActionCommand("Female");
                pst.setString(6, buttonGroup1.getSelection().getActionCommand());
                pst.execute();
                JOptionPane.showMessageDialog(rootPane, "Save Success");
                showTable();
                cleartext();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

