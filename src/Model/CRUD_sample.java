    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int YesOrNo = JOptionPane.showConfirmDialog(null, "Do You Want to Delete the Record", "Warning!", JOptionPane.YES_NO_OPTION);
        if (YesOrNo == JOptionPane.YES_OPTION) {

            try {
                String sql = "DELETE FROM tbl_crud WHERE id = ?";
                pst = conn.prepareStatement(sql);
                pst.setString(1, txt_id.getText());
                pst.execute();
                JOptionPane.showMessageDialog(rootPane, "Deleted Success");
                showTable();
                cleartext();

            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void txt_contactKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_contactKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c) || c == KeyEvent.VK_DELETE)) {
            evt.consume();
            getToolkit().beep();
        }

        if (txt_contact.getText().length() > 11) {
            evt.consume();
            getToolkit().beep();
        }
    }//GEN-LAST:event_txt_contactKeyTyped

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();

        txt_id.setText(model.getValueAt(row, 0).toString());
        txt_fullname.setText(model.getValueAt(row, 1).toString());
        txt_contact.setText(model.getValueAt(row, 2).toString());
        txt_email.setText(model.getValueAt(row, 3).toString());
        txt_address.setText(model.getValueAt(row, 4).toString());
        cmb_job.setSelectedItem(model.getValueAt(row, 5).toString());

        String gen = model.getValueAt(row, 6).toString();
        if ("Male".equals(gen)) {
            radio_male.setSelected(true);
        } else if ("Female".equals(gen)) {
            radio_female.setSelected(true);
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int x = JOptionPane.showConfirmDialog(null, "Are you sure you want to update?", "System message!", JOptionPane.OK_OPTION);
        if (x == 0) {

            try {
                String sql = "UPDATE `tbl_crud` SET `fullname` = ?, `contact` = ?, `email` = ?, `address` = ?, `job` = ?, `gender` = ? WHERE `id` = ?";
                pst = conn.prepareStatement(sql);
                pst.setString(1, txt_fullname.getText());
                pst.setString(2, txt_contact.getText());
                pst.setString(3, txt_email.getText());
                pst.setString(4, txt_address.getText());
                pst.setString(5, cmb_job.getSelectedItem().toString());
                radio_male.setActionCommand("Male");
                radio_female.setActionCommand("Female");
                pst.setString(6, buttonGroup1.getSelection().getActionCommand());
                pst.setString(7, txt_id.getText());
                pst.execute();
                JOptionPane.showMessageDialog(rootPane, "Update Success");
                showTable();
                cleartext();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

